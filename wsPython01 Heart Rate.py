#โจทย์ข้อที่ 1

# รับค่าอายุจากผู้ใช้
age = int(input("อายุ : "))

# คำนวณอัตราการเต้นของหัวใจสูงสุด (mhr)
mhr = 220 - age

# คำนวณขอบเขตของ Zone 2
zone2min = 0.60 * mhr
zone2max = 0.70 * mhr

# แสดงผลลัพธ์
print("ค่า mhr :", mhr)
print("อัตราการเต้นของหัวใจสูงสุดที่เหมาะสม")
print("ค่าต่ำสุดของ Zone 2:", zone2min)
print("ค่าสูงสุดของ Zone 2:", zone2max)
      
